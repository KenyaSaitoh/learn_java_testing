package pro.kensait.java.calc.normal;

/*
 * 通常の計算機（状態を保持しない）
 */
public class Calculator {

    // 足し算を実行する［1］
    public int add(int param1, int param2) {
        return param1 + param2;
    }

    // 引き算を実行する［2］
    public int subtract(int param1, int param2) {
        return param1 - param2;
    }
}